/*
 * Configure colours here
 */

/* Colours 1 through 7 */
#define COL_FF		"\033[31m"	/* 0xFF byte */
#define COL_OFFSET	"\033[32m"	/* Green */
#define	COL_ASCII	"\033[34m"	/* Blue */
#define	COL_ASCII_BAR	"\033[36m"	/* The | around the ascii */
#define COL_ZERO	"\033[33m"	/* 0x00 byte */

/* Text modifiers */
#define RESET	"\033[0m"	/* Reset all modifiers */
#define	BOLD	"\033[1m"
#define	ITALIC	"\033[3m"
#define ULINE	"\033[4m"
#define BLINK	"\033[5m"
